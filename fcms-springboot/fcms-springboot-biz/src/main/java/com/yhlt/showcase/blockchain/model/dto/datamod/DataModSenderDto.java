package com.yhlt.showcase.blockchain.model.dto.datamod;

import java.util.List;

/**
 * 数据模块写入的信息
 * @author stone
 * @date 2018年3月5日 上午9:11:06
 */
public class DataModSenderDto {
	/**
	 * 发送地址
	 */
	private String senditemer;
	/**
	 *发送条数
	 */
	private int items;
	/**
	 * 确认数量
	 */
	private int confirmed;
	/**
	 * 第一次写入情况
	 */
	private List<DataModItem> first;
	/**
	 * 最后一次写入情况
	 */
	private List<DataModItem> last;
	public String getSenditemer() {
		return senditemer;
	}
	public void setSenditemer(String senditemer) {
		this.senditemer = senditemer;
	}
	public int getItems() {
		return items;
	}
	public void setItems(int items) {
		this.items = items;
	}
	public int getConfirmed() {
		return confirmed;
	}
	public void setConfirmed(int confirmed) {
		this.confirmed = confirmed;
	}
	public List<DataModItem> getFirst() {
		return first;
	}
	public void setFirst(List<DataModItem> first) {
		this.first = first;
	}
	public List<DataModItem> getLast() {
		return last;
	}
	public void setLast(List<DataModItem> last) {
		this.last = last;
	}
	@Override
	public String toString() {
		return "DataModSenderDto [senditemer=" + senditemer + ", items=" + items + ", confirmed=" + confirmed
				+ ", first=" + first + ", last=" + last + "]";
	}
}
