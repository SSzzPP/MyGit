package com.yhlt.showcase.blockchain.model.dto.transaction;
/**
 * 钱包交易vin
 * @author stone
 * @date 2018年3月6日 上午9:48:58
 */
public class WalletTransactionVin extends WalletTransactionVInfo{
	private String txid;
	private long vout;
	public String getTxid() {
		return txid;
	}
	public void setTxid(String txid) {
		this.txid = txid;
	}
	public long getVout() {
		return vout;
	}
	public void setVout(long vout) {
		this.vout = vout;
	}
	public WalletTransactionVin() {
		super();
		this.txid="";
	}
	public WalletTransactionVin(WalletTransactionVInfo info){
		super(info);
		txid = "";
	}
}
