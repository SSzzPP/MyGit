package com.yhlt.showcase.venue.dto;

import java.io.Serializable;

/**
 * 场馆使用分析
 * 
 * @author linannan
 * 
 */
public class VenueAnalysisDto implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 3195662154407787050L;
	/**
	 * 设施ID
	 */
	private Long id;
	/**
	 * 场馆ID
	 */
	private Long venueId;

	/**
	 * 设施名称
	 */
	private String equipmentName;
	/**
	 * 今日预约数量
	 */
	private Integer todayCnt;
	/**
	 * 本周预约数量
	 */
	private Integer weekCnt;
	/**
	 * 本月预约数量
	 */
	private Integer monthCnt;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Long getVenueId() {
		return venueId;
	}

	public void setVenueId(Long venueId) {
		this.venueId = venueId;
	}

	public String getEquipmentName() {
		return equipmentName;
	}

	public void setEquipmentName(String equipmentName) {
		this.equipmentName = equipmentName;
	}

	public Integer getTodayCnt() {
		return todayCnt;
	}

	public void setTodayCnt(Integer todayCnt) {
		this.todayCnt = todayCnt;
	}

	public Integer getWeekCnt() {
		return weekCnt;
	}

	public void setWeekCnt(Integer weekCnt) {
		this.weekCnt = weekCnt;
	}

	public Integer getMonthCnt() {
		return monthCnt;
	}

	public void setMonthCnt(Integer monthCnt) {
		this.monthCnt = monthCnt;
	}

}
